var a = 3;

console.log(a);

// Usando a palavra reservada VAR, poderá redeclarar uma variavel.
var a = 6;
console.log(a);
//Por isso devemos não utilizar o VAR

let b = 6;
console.log(b);

//Não se pode redeclarar uma variavel com a palavra reservada LET
b = 9;
console.log(b);

const c = 500;

//Não pode variar o seu valor, com const

console.log(a, b, c);